# OPERADORES ARITMETICOS
'''
Operador        Simbolo

Suma            +
Resta           -
Multiplicacion  *
Division        /
Modulo          %
Potenciacion    **
'''

var_a = 83
var_b = 37

print("OPERADOR SUMA: ")
print("El resultado de la suma {} + {} = {}".format(var_a, var_b, var_a+var_b))

print("OPERADOR RESTA: ")
print("El resultado de la multiplicacion {} - {} = {}".format(var_a, var_b, var_a-var_b))

print("OPERADOR MULTIPLICACION: ")
print("El resultado de la multiplicacion {} * {} = {}".format(var_a, var_b, var_a*var_b))

print("OPERADOR DIVISION: ")
print("El resultado de la division {} / {} = {}".format(var_a, var_b, var_a/var_b))

print("OPERADOR MODULO: ")
print("El resultado de modulo {} % {} = {}".format(var_a, var_b, var_a%var_b))

print("OPERADOR POTENCIACION: ")
print("El resultado de potenciacion {} ** {} = {}".format(var_a, var_b, var_a**var_b))
